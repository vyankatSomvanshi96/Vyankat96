package Studies;

public class Primitivestudy {

	public static void main(String[] args) {
		

		// Non-Decimal
		byte v;
		v=102;
	    System.out.println(v);
		
		short c;
		c=3333;
		System.out.println(c);
		
		int d;
		d=23456;
		System.out.println(d);
		
		long z;
		z=2356717;
		System.out.println(z);
		
		// For Decimal
		
		float a;
		a=33.45563687f;
		System.out.println(a);
		
		float k;
		float l;
		float add;
		
		k=2.3f;
		l=2.4f;
		add=k+l;
		System.out.println(add);
		
		
		double b;
		b=89.43567827563;
		System.out.println(b);
		
		// Char
		char blood;
		blood ='A';
		System.out.println("My blood group is "+blood);
		
		// Boolean
		
		boolean fail;
		 fail=false;
		System.out.println(fail);
		
		
		
		
		
		
		
		
		
		
	
		
		
		
	}

}
