package ControlStatement;

public class SwitchSty {

	public static void main(String[] args) {
		
		
		String day="wend";
		
		switch (day) 
		{
		case "Mon": System.out.println("this is 1st day");
		break;
		
		case "Tue": System.out.println("this is 2nd day");
		break;

		case "wend": System.out.println("this is 3rd day");
		break;
		
		case "Thur" : System.out.println("this is 4th day");
		
		default:  System.out.println("Pls enter valid day");
		break;
		}
		
		
		
		
		
	}

}
